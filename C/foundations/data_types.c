#include <stdio.h>
#include <stdlib.h>

/* Examples of data types we get in C and how to use them syntactically correct. */

int main()
{
    int age = 22; //int data type takes whole numbers

    double gpa = 53.6; //double data type takes decimal numbers

    char grade = 'C'; //char data type takes a single character

    char phrase[] = "You passed!"; //char data type with '[]' takes multiple characters to form a STRING ARRAY

    //unlike other data types, you CAN'T MODIFY STRING ARRAY


    return 0;
}